static bool triggered = false;
static mcpwm_cmpr_handle_t servo1;
static mcpwm_cmpr_handle_t servo2;

bool laser_is_triggered()
{
    return triggered;
}

void laser_set_position(int8_t horizontal_angle, int8_t vertical_angle)
{
    int8_t a1 = servo1_table[horizontal_angle + 128];
    int8_t a2 = servo1_table[vertical_angle + 128];

    servo_set_angle(&servo1, -a1);
    servo_set_angle(&servo2, a2);
}

void laser_trigger(bool on)
{
    triggered = on;
    gpio_set_level(GPIO_LASER_TRIGGER, on);
    servo_enable(&servo1, on);
    servo_enable(&servo2, on);
}

void laser_init()
{
    servo_create(GPIO_LASER_SERVO1, &servo1);
    servo_create(GPIO_LASER_SERVO2, &servo2);
    servo_set_angle(&servo1, 0);
    servo_set_angle(&servo2, 0);

    gpio_config_t o_conf = {};
    o_conf.pin_bit_mask |= (1ULL << GPIO_LASER_TRIGGER);
    o_conf.intr_type = GPIO_INTR_DISABLE;
    o_conf.mode = GPIO_MODE_OUTPUT;
    o_conf.pull_up_en = 0;
    o_conf.pull_down_en = 1;
    gpio_config(&o_conf);
    gpio_set_level(GPIO_LASER_TRIGGER, 0);
}
